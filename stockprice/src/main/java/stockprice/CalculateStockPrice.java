public class CalculateStockPrice {

	public Integer getTotalPriceByItem(String item) {

		Integer itemPrice = 0;
		if (item.equals("A"))
			return itemPrice = 50;
		else if (item.equals("B"))
			return itemPrice = 30;
		
		return itemPrice;
	}

	
}
